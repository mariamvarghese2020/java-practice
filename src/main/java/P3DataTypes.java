public class P3DataTypes {
  public static void main(String[] args) {
    System.out.println("Primitive data types in java : ");
    boolean isWinter=true;
    System.out.println("isWinter boolean can have true /false values " +isWinter);
    char currency ='$';
    System.out.println("char example  "+currency);
    System.out.println("Byte range "+ Byte.MIN_VALUE +" to "+Byte.MAX_VALUE);
    System.out.println("Short range "+Short.MIN_VALUE + "to "+Short.MAX_VALUE);
    System.out.println("Int range "+Integer.MIN_VALUE + "to "+Integer.MAX_VALUE);
    System.out.println("Long range "+Long.MIN_VALUE + "to "+Long.MAX_VALUE);
    System.out.println("Float range "+Float.MIN_VALUE + "to "+Float.MAX_VALUE);
    System.out.println("Double range "+Double.MIN_VALUE + "to "+Double.MAX_VALUE);
  }
}
